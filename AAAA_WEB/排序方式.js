
// 示例 1：

// 输入：arr = [5, 4, 1, 2, 3], fn = (x) => x
// 输出：[1, 2, 3, 4, 5]
// 解释：fn 只是返回传入的数字，因此数组按升序排序。
// 示例 2：

// 输入：arr = [{"x": 1}, {"x": 0}, {"x": -1}], fn = (d) => d.x
// 输出：[{"x": -1}, {"x": 0}, {"x": 1}]
// 解释：fn 返回 "x" 键的值，因此数组根据该值排序。


var sortBy = function(arr, fn) {
    return arr.sort((a,b) => fn(a) - fn(b))
};

